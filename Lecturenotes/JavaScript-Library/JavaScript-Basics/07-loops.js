//Loop from  0 to 50 by 5s.

for(var i = 0; i <= 50; i += 5){
    console.log(i);
}

//Loop from 20 to 1 by -1s.

for (var i = 20; i >= 1; i--){
    console.log(i);
}

//Loop starting at 3, and increments by 5, but doesn't go over 30.

for(var i = 3; i < 30; i += 5){
    console.log(i)
}
